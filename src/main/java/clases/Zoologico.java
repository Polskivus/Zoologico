package clases;

public class Zoologico {

	/*Creacion de sus datos*/
	
	private int idZoologico;
	private String nombreZoo;
	private String ciudadZoo;
	private String paisZoo;
	private int presupuestoZoo;
	private int superficieZoo;

	/*Creacion de setters y getters*/
	
	public int getIdZoologico() {
		return idZoologico;
	}

	public void setIdZoologico(int idZoologico) {
		this.idZoologico = idZoologico;
	}

	public String getNombreZoo() {
		return nombreZoo;
	}

	public void setNombreZoo(String nombreZoo) {
		this.nombreZoo = nombreZoo;
	}

	public String getCiudadZoo() {
		return ciudadZoo;
	}

	public void setCiudadZoo(String ciudadZoo) {
		this.ciudadZoo = ciudadZoo;
	}

	public String getPaisZoo() {
		return paisZoo;
	}

	public void setPaisZoo(String paisZoo) {
		this.paisZoo = paisZoo;
	}

	public int getPresupuestoZoo() {
		return presupuestoZoo;
	}

	public void setPresupuestoZoo(int presupuestoZoo) {
		this.presupuestoZoo = presupuestoZoo;
	}

	public int getSuperficieZoo() {
		return superficieZoo;
	}

	public void setSuperficieZoo(int superficieZoo) {
		this.superficieZoo = superficieZoo;
	}

}
